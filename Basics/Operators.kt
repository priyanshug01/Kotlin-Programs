fun main() {
    var x = 5
    var y = 6
    println("Addition: ${x+y}")
    println("Subtraction: ${x-y}")
    println("Multiplication: ${x*y}")
    println("Division: ${x/y}")
    println("Modulus: ${x%y}")
    var z = 3.0//TypeCasting is Automatic
    println("Addition: ${x+z}")
    println("Subtraction: ${x-z}")
    println("Multiplication: ${x*z}")
    println("Division: ${x/z}")
    println("Modulus: ${x%z}")
    println("3 + 3 * 4 / 2: ${3 + 3 * 4 / 2}")
    println("X = ${x++}")
    println("X = ${++x}")
    println("X = ${--x}")
    println("X = ${x--}")
    println("X = $x")

}
/*(+,-,*,/,%) are operators and x,y are operands
Arithmetic Operators
(>,<.>=,<=,==,!=) Relational Operators
(+=,-=.*=,/=,%=,!=) Assignment Operators
(++,--) Unary Operator
(&&,||,!) Logical Operator
*/